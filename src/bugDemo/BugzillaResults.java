package bugDemo;

public class BugzillaResults {

	private String project_name;
	private String bug_priority;
	private String project_bugs;

	public String getProject_name() {
		return project_name;
	}

	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}

	public String getBug_priority() {
		return bug_priority;
	}

	public void setBug_priority(String bug_priority) {
		this.bug_priority = bug_priority;
	}

	public String getProject_bugs() {
		return project_bugs;
	}

	public void setProject_bugs(String project_bugs) {
		this.project_bugs = project_bugs;
	}

}
